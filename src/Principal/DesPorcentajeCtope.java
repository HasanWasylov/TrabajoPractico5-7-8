package Principal;

public class DesPorcentajeCtope {
	private int codigo;
	private int cant;
	
	public DesPorcentajeCtope(int codigo, int cant) {
		this.codigo = codigo;
		this.cant = cant;
	}
	
	public DesPorcentajeCtope() {
		
	}

	public int getCodigo() {
		return codigo;
	}

	public void setCodigo(int codigo) {
		this.codigo = codigo;
	}

	public int getCant() {
		return cant;
	}

	public void setCant(int cant) {
		this.cant = cant;
	}
	
	
	
}
